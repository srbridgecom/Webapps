<strong>Canada Struck First with Tariffs on the U.S.—Trump Responds, Yet Only His Actions Draw Fire</strong>

Canada placed tariffs on the USA first—before Trump even took office—but it seems like everyone only barks when Trump acts. Let’s set the record straight: Canada kicked off this trade war, targeting everyday goods and U.S. farmers, and Trump’s just following suit to combat it. Here’s the timeline, the facts, and the bigger picture.
<ul>
 	<li>Prior to 2025: Canada imposed tariffs on U.S. everyday and farm products, including a staggering 250% to 390% tariff on American farmers’ dairy products and more, under its “anti-American farmer tariff” system.</li>
 	<li>March 11, 2025: Canada slapped a 25% tariff on electricity exported to the USA, hitting American homes and businesses hard.</li>
 	<li>March 12, 2025: President Trump responded with a 50% tariff on steel and aluminum imported from Canada into the USA.</li>
 	<li>April 2, 2025: Trump announced plans to increase tariffs on cars from Canada, escalating the tit-for-tat.</li>
</ul>
Trump has also demanded that Canada drop its crippling 250% to 390% tariffs on U.S. farmers, dairy products, and other goods. On top of that, he’s charging Canada $200 billion for military security, arguing they’ve leaned on U.S. protection too long. So why’s the spotlight only on Trump?
<h4 class="western">Trade Snapshot: The Numbers Don’t Lie</h4>
Here’s the trade balance between the U.S. and Canada over the past two years:
<ul>
 	<li>2023: The USA exported $354.4 billion to Canada, while importing $418.6 billion from Canada—a trade deficit of $64.3 billion.</li>
 	<li>2024: The USA exported $349.4 billion to Canada, while importing $412.7 billion—a deficit of $63.3 billion.</li>
</ul>
The U.S. keeps importing more than it exports, especially energy like crude oil and natural gas. Canada’s tariffs only widen that gap—so why aren’t they the ones under fire?
<h4 class="western">Canada’s Pre-Trump Tariffs: A Longstanding Burden</h4>
