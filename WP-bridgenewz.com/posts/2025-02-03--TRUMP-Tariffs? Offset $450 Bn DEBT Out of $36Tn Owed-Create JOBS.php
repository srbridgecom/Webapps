<b>Trump Imposes Major Tariffs on Canada, Mexico, and China, Citing National Emergency</b>

In a sweeping move, President Donald Trump, 78, announced via Truth Social on Sunday that the United States will impose a <b>25% tariff on imports from Canada and Mexico</b> and a <b>10% tariff on imports from China</b>, citing a <b>national emergency</b> over the failure of these nations to curb the flow of <b>deadly drugs</b> and <b>illegal immigration</b> into the U.S.

“This will be the golden age of America! Will there be some pain? Yes, maybe (and maybe not!). But we will make America great again, and it will all be worth the price that must be paid,” Trump declared in all caps.
<h3><b>Citing Fentanyl and Immigration Crisis</b></h3>
During a speech in the Oval Office, Trump elaborated on the reasoning behind the tariffs, stating:

“We have big deficits, as you know, with all three of them. And in one case, they’re sending massive amounts of fentanyl, killing hundreds of thousands of people a year, with the fentanyl. And in the other two cases, they’re making it possible for this poison to get in.”

A <b>White House statement</b> echoed these sentiments, asserting that the extraordinary threat posed by <b>illegal immigration and fentanyl</b> constitutes a <b>national emergency</b> under the <b>International Emergency Economic Powers Act (IEEPA)</b>, which grants the president the authority to impose trade restrictions.

“The extraordinary threat posed by illegal aliens and drugs, including deadly fentanyl, constitutes a national emergency,” the statement read.
<h3><b>Tariffs Aimed at Leveling Trade Imbalance</b></h3>
While Trump emphasized the fentanyl crisis as a key justification, he also framed the tariffs as part of an effort to address what he considers to be an <b>unfair trade relationship</b>.

“The USA has major deficits with Canada, Mexico, and China (and almost all countries!), owes 36 Trillion Dollars, and we’re not going to be the ‘Stupid Country’ any longer,” Trump said.

The <b>U.S. trade deficit with these three nations exceeded $450 billion in 2023</b>, and the administration argues that these tariffs will help to <b>reduce that gap</b> while strengthening the American economy.

The White House underscored that the <b>American market is a privilege</b>, pointing out that while:
<ul>
 	<li aria-level="1"><b>Trade accounts for 67% of Canada’s GDP</b>,</li>
 	<li aria-level="1"><b>73% of Mexico’s GDP</b>,</li>
 	<li aria-level="1"><b>And 37% of China’s GDP</b>,</li>
</ul>
Trade accounts for only <b>24% of the U.S. GDP</b>.

Further highlighting the economic disparity, the U.S. administration noted that:
<ul>
 	<li aria-level="1"><b>Roughly 75% of Canada’s exports go to the U.S.</b>, while only <b>13% of American exports go to Canada</b>.</li>
 	<li aria-level="1"><b>About 80% of Mexican exports flow to the U.S.</b>, while only <b>16% of U.S. exports go to Mexico</b>.</li>
 	<li aria-level="1"><b>The U.S. trade deficit in goods was the world’s largest in 2023, surpassing $1 trillion</b>.</li>
</ul>
<h3><b>Trump’s Strong Message to Trading Partners</b></h3>
Trump did not hold back in his criticism, <b>accusing Canada and Mexico of failing to stop fentanyl</b> and blaming China for <b>allowing the flow of precursor chemicals used by drug cartels</b>.

Additionally, he <b>doubled down on his long-standing argument</b> that the U.S. is <b>subsidizing</b> Canada by importing its goods rather than producing them domestically.

“Make your product in the USA and there are no tariffs!” Trump wrote on Truth Social.

“Why should the United States lose trillions of dollars in subsidizing other countries, and why should these other countries pay a small fraction of the cost of what USA citizens pay for drugs and pharmaceuticals, as an example?”

He went even further, claiming that <b>Canada “would cease to exist as a viable country” if the U.S. cut off trade</b> and even <b>suggested once again that Canada should become the 51st state</b>.
<h3><b>Additional Trade Restrictions</b></h3>
The latest tariffs also come with <b>a 10% tax on Canadian energy exports</b>, with Trump asserting:

“We don’t need anything they have. We have unlimited Energy, should make our own Cars, and have more Lumber than we can ever use.”

Furthermore, the administration is <b>eliminating the "de minimis" exemption</b> for packages under <b>$800</b>, a move aimed directly at <b>Chinese retailers like Alibaba, Temu, and Shein</b>, which have <b>saved hundreds of millions per year</b> by shipping cheap products <b>directly to American consumers without paying duties</b>.
<h3><b>Canada, Mexico, and China Retaliate</b></h3>
While Trump recently saw <b>success in strong-arming Colombian President Gustavo Petro</b> into <b>accepting deported migrants</b> by slapping <b>a 25% tariff on Colombian imports</b>, Canada, Mexico, and China are <b>pushing back hard</b> against the latest trade measures.

<b>Canada and Mexico</b> quickly announced <b>25% counter-tariffs on U.S. goods</b>, while <b>China has vowed to challenge the new tariffs at the World Trade Organization (WTO)</b>.
<h3><b>Potential Economic Fallout</b></h3>
The U.S. imports approximately <b>$1.4 trillion worth of goods annually</b> from these three countries, accounting for <b>over 40% of total U.S. imports</b>.

According to <b>Bloomberg Economics</b>, this move could <b>significantly impact the American economy</b>, warning that:
<ul>
 	<li aria-level="1">The <b>average U.S. tariff rate could spike from under 3% to 10.7%</b>.</li>
 	<li aria-level="1"><b>U.S. GDP could decline by 1.2%</b>.</li>
 	<li aria-level="1"><b>Inflation could increase by 0.7%</b>.</li>
</ul>
Despite the risks, Trump remains steadfast, betting that <b>America’s trading partners need the U.S. market far more than the U.S. needs their goods</b>.
<h3><b>What Comes Next?</b></h3>
With <b>retaliatory tariffs now in play</b> and <b>China preparing a legal battle at the WTO</b>, the world may be on the brink of a <b>full-blown trade war</b>. Whether Trump’s tariffs bring the economic boost he promises—or lead to increased costs for American consumers—remains to be seen.

