<strong>HOW TO OBTAIN?</strong>

From Monday, Jan. 27, through Saturday, Feb. 1, guests can enjoy one free Original Chick-fil-A Chicken Sandwich, Spicy Chicken Sandwich, or Grilled Chicken Sandwich.

To claim the offer, guests need to:
<ul>
 	<li>Download the Chick-fil-A app</li>
 	<li>Log in or create an account</li>
 	<li>Choose a participating restaurant</li>
 	<li>Redeem the offer</li>
</ul>
The promotion is available through the app while supplies last and is limited to one sandwich per person, per Chick-fil-A account.

